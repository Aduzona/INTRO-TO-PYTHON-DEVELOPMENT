# Looping with a condition
# we use while loops when something is going to change automatically 

names = ['Christopher', 'Susan']
index = 0
while index < len(names):
    print(names[index])
    #Change the condition!!
    index = index + 1


print()

people= ['Diego','Frank','Victor']
index = 0
while index < len(people):
    print(people[index])
    index = index + 1
